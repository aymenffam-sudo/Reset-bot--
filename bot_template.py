# Main
# ━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━
async def main():
    global telethon_client

    if not os.path.exists(SESSIONS_FILE):
        save_sessions({"credentials": {}, "sessions": {}})

    # Connect Telethon using existing session
    telethon_client = TelegramClient(SESSION_NAME, API_ID, API_HASH)
    await telethon_client.connect()
    if not await telethon_client.is_user_authorized():
        logger.error("❌ Telethon session not found! Run setup_session.py first.")
        return

    logger.info(f"✅ Child bot ready | bot started")

    app = Application.builder().token(BOT_TOKEN).build()

    login_conv = ConversationHandler(
        entry_points=[CommandHandler("login", cmd_login)],
        states={WAITING_LOGIN: [MessageHandler(filters.TEXT & ~filters.COMMAND, receive_credentials)]},
        fallbacks=[CommandHandler("cancel", cancel_login)],
    )
    app.add_handler(login_conv)
    app.add_handler(CommandHandler("start",         cmd_start))
    app.add_handler(CommandHandler("help",          cmd_help))
    app.add_handler(CommandHandler("logout",        cmd_logout))
    app.add_handler(CommandHandler("fluorite",      cmd_fluorite))
    app.add_handler(CommandHandler("addaccount",    cmd_addaccount))
    app.add_handler(CommandHandler("removeaccount", cmd_removeaccount))
    app.add_handler(CommandHandler("listaccounts",  cmd_listaccounts))

    async with app:
        await app.initialize()
        await app.start()
        await app.updater.start_polling(drop_pending_updates=True)
        await asyncio.Event().wait()
        await app.updater.stop()
        await app.stop()

    await telethon_client.disconnect()


if __name__ == "__main__":
    try:
        asyncio.run(main())
    except KeyboardInterrupt:
        pass
